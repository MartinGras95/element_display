$(document).ready(function () {

    // background color selections

    $("#btn-light").click(function (e) { 
        e.preventDefault();
        $("body").css("background-color","white");
    });

    $("#btn-night").click(function (e) { 
        e.preventDefault();
        $("body").css("background-color","darkslategray");
    });

    $("#btn-terrible").click(function (e) { 
        e.preventDefault();
        $("body").css("background-color","lawngreen");
    });

    // Button animations








});